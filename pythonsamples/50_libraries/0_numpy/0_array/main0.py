import numpy as np

# ----------------------------------------------------
# ----------------------------------------------------
# Arrays
# ----------------------------------------------------
# ----------------------------------------------------
my_list = np.random.randn(20)
print(my_list)


# ----------------------------------------------------
# Count
# ----------------------------------------------------
print(my_list.size)


# ----------------------------------------------------
# Get data
# ----------------------------------------------------
val = my_list[0]
print(val)

# ----------------------------------------------------
# Set data
# ----------------------------------------------------
my_list[0] = 999
print(my_list[0])


# ----------------------------------------------------
# Append value
# ----------------------------------------------------
newArr = np.append(my_list, [98, 99])
print(newArr)

# ----------------------------------------------------
# Delete value (at index)
# ----------------------------------------------------
newArr2 = np.delete(my_list, 0)
print(newArr2)

# ----------------------------------------------------
# Find a value
# ----------------------------------------------------
idx = np.where(newArr == 99)
print(idx)

# ----------------------------------------------------
# Apply function (to each item)
# ----------------------------------------------------
print("map")
myFunc = lambda x: x * 100
newArr3 = myFunc(my_list)
print(newArr3)

