import numpy as np
import pandas as pd

# ----------------------------------------------------
# ----------------------------------------------------
# DataFrames
# ----------------------------------------------------
# ----------------------------------------------------
my_df = pd.DataFrame(np.random.randn(3, 4), ["A", "B", "C"], ["W", "X", "Y", "Z"])
print(my_df)


# ----------------------------------------------------
# Count
# ----------------------------------------------------

# all
print(my_df.shape)

# rows
print(my_df.shape[0])

# columns
print(my_df.shape[1])


# ----------------------------------------------------
# Value Counts = Histogram
# ----------------------------------------------------
print(my_df["X"].value_counts())


# ----------------------------------------------------
# Sum
# ----------------------------------------------------
print(my_df["X"].sum())

# ----------------------------------------------------
# Cumulative Sum
# ----------------------------------------------------
cumsum = my_df["X"].cumsum()
print(cumsum)
cumsum.plot()


# cumulate minimum
print(my_df["X"].cummin())

# cumulate maximum
print(my_df["X"].cummax())


# ----------------------------------------------------
# Min, Max
# ----------------------------------------------------
print(my_df["X"].min())
print(my_df["X"].max())


# ----------------------------------------------------
# Avg
# ----------------------------------------------------

# The mean or average is (1+1+2+3+4)/5 = 2.2
# The median is "2" (the central value).
# The mode is "1" (it occurs most often).
# The midrange is (4+1)/2 = 2.5

print(my_df["X"].mean())  # arithmetic average
print(my_df["X"].median())  # median
print(my_df["X"].mode())  # mode


# ----------------------------------------------------
# Variance
# ----------------------------------------------------
print(my_df["X"].var())


# ----------------------------------------------------
# Standard deviation
# ----------------------------------------------------
print(my_df["X"].std(ddof=2))
