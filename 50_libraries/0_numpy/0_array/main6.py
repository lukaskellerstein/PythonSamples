import numpy as np

# ----------------------------------------------------
# ----------------------------------------------------
# Arrays
# ----------------------------------------------------
# ----------------------------------------------------
my_list = np.arange(25)
print(my_list)

# ------------------------
# select by index
# ------------------------
res1 = my_list[5:10]
print(res1)

res2 = my_list[2:]
print(res2)

res3 = my_list[:2]
print(res3)


# ------------------------
# change by index
# ------------------------

my_list[5:10] = 100
print(my_list)
